package com.company.datastructure;

public class Location {
  private Integer xIndis;
  private Integer yIndis;

  public Location(Integer xIndis, Integer yIndis) {
    this.xIndis = xIndis;
    this.yIndis = yIndis;
  }

  public Integer getxIndis() {
    return xIndis;
  }

  public void setxIndis(Integer xIndis) {
    this.xIndis = xIndis;
  }

  public Integer getyIndis() {
    return yIndis;
  }

  public void setyIndis(Integer yIndis) {
    this.yIndis = yIndis;
  }
}
